  optimized = optimized.replace(/🍆/g, ''); // 茄子マークは読み上げでは除去
  
  // 英単語のカタカナ置換（音声エンジン対策）
  optimized = optimized.replace(/TKG/gi, 'たまごかけごはん');
  optimized = optimized.replace(/WiFi/gi, 'ワイファイ');
  optimized = optimized.replace(/Wi-Fi/gi, 'ワイファイ');
