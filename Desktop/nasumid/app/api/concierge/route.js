  optimized = optimized.replace(/¥/g, '');
  optimized = optimized.replace(/,/g, '');
  optimized = optimized.replace(/・/g, '、');
  
  // コロン時間の日本語化 (例: 6:45 -> 6時45分, 9:30 -> 9時30分)
  optimized = optimized.replace(/(\d+):(\d+)/g, '$1時$2分');
  
  // 特殊記号の削除・置換
  optimized = optimized.replace(/[【】]/g, '、');
  optimized = optimized.replace(/[◆◇■□▲▼]/g, '、');
  optimized = optimized.replace(/[\(\)（）]/g, '、');
