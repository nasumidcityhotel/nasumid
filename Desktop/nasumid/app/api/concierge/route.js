    if (q.includes('ベッド') && header.includes('客室')) score += 8;
    if (q.includes('シモンズ') && header.includes('客室')) score += 10;

    // 単語キーワードマッチ
    const keywords = q.split(/[\s,，.．、。？?！!]+/);
